    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
    String marca = jTextField1.getText();
    String modelo = jTextField4.getText();
    String chassi = jTextField2.getText();
    String placa = jTextField3.getText();

    String arcondicionado = "";
    if (jRadioButton1.isSelected()) {
        arcondicionado = "Sim";
    } else if (jRadioButton2.isSelected()) {
        arcondicionado = "NÃ£o";
    }

    TelaDadosCar telaDadosCar = new TelaDadosCar(marca, modelo, chassi, placa, arcondicionado);

    javax.swing.JDesktopPane desktopPane = (javax.swing.JDesktopPane) this.getParent();
    desktopPane.add(telaDadosCar);
    telaDadosCar.setVisible(true);

    // Centraliza a nova tela
    int x = (desktopPane.getWidth() - telaDadosCar.getWidth()) / 2;
    int y = (desktopPane.getHeight() - telaDadosCar.getHeight()) / 2;
    telaDadosCar.setLocation(x, y);

    this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed


